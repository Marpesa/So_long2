#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, int n)
{
	int				i;
	unsigned char	*a;
	unsigned char	*b;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (a[i] == b[i] && i < n - 1)
		i++;
	return (a[i] - b[i]);
}
