#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char		*a;
	size_t		i;

	i = 0;
	a = s;
	while (i < n)
	{
		a[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
