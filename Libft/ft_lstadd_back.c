#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*temp;

	if (!*alst)
	{
		*alst = new;
		return ;
	}
	if (!new)
		return ;
	temp = ft_lstlast(*alst);
	temp->next = new;
}
